package bootcamp;

public class StringPractice {

	public static void main(String[] args) {
		
		String str = "Cydeo";
		
		str = str.concat(" School"); //"Cydeo School"
		
		
		System.out.println(str);
		
		
		System.out.println("--------------------------------------");
		
		StringBuilder sb = new StringBuilder("Cydeo");
		
		sb.append(" School");
		
		System.out.println(sb);
		
		
		
		
		
		
		
	}
	
}
